CREATE TABLE resources (
    resourceid text PRIMARY KEY          -- Example: '0000037fecc4461faf0e49328ae66661',
    projectid text                       -- Example: '00001ccc0e81598c4bd86bacb94d7acb',
    vendorid integer                     -- Example: 430,
    vendor_name text                     -- Example: '100 % Educational Videos',
    project_resource_type text           -- Example: 'Technology',
    item_name text                       -- Example: 'iPod nano 4th Gen 8GB (Black)',
    item_number text                     -- Example: '249995.001',
    item_unit_price real                 -- Example: 149.0,
    item_quantity integer                -- Example: 0,
    FOREIGN KEY (projectid) REFERENCES projects(projectid)
