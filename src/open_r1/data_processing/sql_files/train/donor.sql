CREATE TABLE donations (
    donationid text PRIMARY KEY          -- Example: '000000a91a14ed37bef82e125c102e77',
    projectid text                       -- Example: '00001ccc0e81598c4bd86bacb94d7acb',
    donor_acctid text                    -- Example: '000013be53ed7382ed5d5975eb86eb85',
    donor_city text                      -- Example: 'Peachtree City',
    donor_state text                     -- Example: 'GA',
    donor_zip text                       -- Example: '0',
    is_teacher_acct text                 -- Example: 'f',
    donation_timestamp datetime          -- Example: '2000-03-01 14:24:26',
    donation_to_project real             -- Example: 42.5,
    donation_optional_support real       -- Example: 7.5,
    donation_total real                  -- Example: -15.0,
    dollar_amount text                   -- Example: '10_to_100',
    donation_included_optional_support text -- Example: 't',
    payment_method text                  -- Example: 'no_cash_received',
    payment_included_acct_credit text    -- Example: 'f',
    payment_included_campaign_gift_card text -- Example: 't',
    payment_included_web_purchased_gift_card text -- Example: 'f',
