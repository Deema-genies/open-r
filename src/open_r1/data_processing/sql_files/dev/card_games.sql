CREATE TABLE legalities (
    id integer PRIMARY KEY               -- Example: 1,
    format text                          -- Example: 'commander',
    status text                          -- Example: 'Legal',
    uuid text                            -- Example: '5f8287b1-5bb6-5f4c-ad17-316a40d5bb0c',
    FOREIGN KEY (uuid) REFERENCES cards(uuid)
);

CREATE TABLE sets (
    id integer PRIMARY KEY               -- Example: 1,
    baseSetSize integer                  -- Example: 383,
    block text                           -- Example: 'Core Set',
    booster text                         -- Example: '{'default': {'boosters': [{'contents': {'basic': 1, 'common': 10, 'rare': 1, 'un',
    code text                            -- Example: '10E',
    isFoilOnly integer                   -- Example: 0,
    isForeignOnly integer                -- Example: 0,
    isNonFoilOnly integer                -- Example: 0,
    isOnlineOnly integer                 -- Example: 0,
    isPartialPreview integer             -- Example: 0,
    keyruneCode text                     -- Example: '10E',
    mcmId integer                        -- Example: 74,
    mcmIdExtras integer                  -- Example: 3209,
    mcmName text                         -- Example: 'Tenth Edition',
    mtgoCode text                        -- Example: '10E',
    name text                            -- Example: 'Tenth Edition',
    parentCode text                      -- Example: 'JMP',
    releaseDate date                     -- Example: '2007-07-13',
    tcgplayerGroupId integer             -- Example: 1,
    totalSetSize integer                 -- Example: 508,
    type text                            -- Example: 'core'
);

CREATE TABLE set_translations (
    id integer PRIMARY KEY               -- Example: 1,
    language text                        -- Example: 'Chinese Simplified',
    setCode text                         -- Example: '10E',
    translation text                     -- Example: '核心系列第十版',
    FOREIGN KEY (setCode) REFERENCES sets(code)
);

CREATE TABLE rulings (
    id integer PRIMARY KEY               -- Example: 1,
    date date                            -- Example: '2007-07-15',
    text text                            -- Example: 'You draw the card when Bandage resolves, not when the damage is actually prevent',
