    id integer PRIMARY KEY               -- Example: 1,
    flavorText text                      -- Example: '„Es ist der Wille aller, und meine Hand, die ihn ausführt."',
    language text                        -- Example: 'German',
    multiverseid integer                 -- Example: 148411,
    name text                            -- Example: 'Ausgewählter der Ahnfrau',
    text text                            -- Example: 'Erstschlag (Diese Kreatur fügt Kampfschaden vor Kreaturen ohne Erstschlag zu.)
W',
    type text                            -- Example: 'Kreatur — Mensch, Kleriker',
    uuid text                            -- Example: '5f8287b1-5bb6-5f4c-ad17-316a40d5bb0c',
